def index(request):
    return render(request, "my_blog/index.html", {"posts": posts})


def all_posts(request):
    return render(request, "my_blog/all_posts.html", {"posts": posts})


def blog_post(request, slug):
    blog = None
    for post in posts:
        if slug in post.values():
            blog = post
            break
    return render(request, "my_blog/blog_post.html", {"blog": blog})
